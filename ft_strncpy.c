#include <string.h>

char		*ft_strncpy(char *dst, const char *src, size_t n)
{
	int i;

	i = 0;
	while (src[i] != '\0' && i < (int)n)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
